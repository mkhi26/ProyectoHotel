        this.cbxProducto.setSelectedIndex(posicionProducto);
        this.txtCodigoProducto.setText(Integer.toString(idProducto));
        this.spnCantidad.setValue(objLista.getCantidad());

        this.btnCaja.setEnabled(false);
        this.btnCancelar.setEnabled(true);
        this.btnEditar.setEnabled(true);
        this.btnEliminar.setEnabled(true);
        this.btnGuardar.setEnabled(false);
        this.btnNuevo.setEnabled(false);
        this.btnReportes.setEnabled(false);



    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed

        try {

            int cantidad;
            Producto producto;

            int index;
            if (agregando) {

                if (codigo) {

                    index = PantallaPrincipal.gProd.buscarPorId(Integer.parseInt(txtCodigoProducto.getText()));

                    codigo = false;
                } else {
                    index = cbxProducto.getSelectedIndex();
                }
                cantidad = (int) this.spnCantidad.getValue();
                if (cantidad < 1) {
                    JOptionPane.showMessageDialog(null, "Ingrese una cantidad para el producto seleccionado");
                } else {
                    producto = PantallaPrincipal.gProd.getProducto(index);

                    ListaProductos objListaProductos = new ListaProductos();
                    objListaProductos.setCantidad(cantidad);
                    objListaProductos.setProducto(producto);

                    PantallaPrincipal.gLstProductos.agregarCarrito(objListaProductos);
                    this.actualizarElementosEnTabla();
                    agregando = false;

                }
            } else {
                if (codigo) {

                    index = PantallaPrincipal.gProd.buscarPorId(Integer.parseInt(txtCodigoProducto.getText()));

                    codigo = false;
                } else {
                    index = cbxProducto.getSelectedIndex();
                }
                cantidad = (int) this.spnCantidad.getValue();
                if (cantidad < 1) {
                    JOptionPane.showMessageDialog(null, "Ingrese una cantidad para el producto seleccionado");
                } else {
                    producto = PantallaPrincipal.gProd.getProducto(index);

                    ListaProductos objListaProductos = new ListaProductos();
                    objListaProductos.setCantidad(cantidad);
                    objListaProductos.setProducto(producto);

                    PantallaPrincipal.gLstProductos.modificar(objListaProductos, posicionElemento);
                    this.actualizarElementosEnTabla();

                }

            }
            this.btnCaja.setEnabled(true);
            this.btnCancelar.setEnabled(false);
            this.btnEditar.setEnabled(false);
            this.btnEliminar.setEnabled(false);
            this.btnGuardar.setEnabled(false);
            this.btnNuevo.setEnabled(true);
            this.btnReportes.setEnabled(true);
            this.cbxProducto.setEnabled(false);

            this.txtCodigoProducto.setEnabled(false);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error al guardar o editar" + e);
        }


    }//GEN-LAST:event_btnGuardarActionPerformed


    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed

        try {
            // TODO add your handling code here:
            String nombre, descripcion, uMedida;
            int existencia;
            Proveedor objProveedor;
            float precioVenta;
            Date fechaVencimiento;

            if (agregando) {
                nombre = this.txtNombreProducto.getText();
                descripcion = this.txtDescripcion.getText();
                uMedida = this.txtUnidadMedida.getText();
                existencia = Integer.parseInt(this.txtExistencia.getText());

                //Obtener objeto proveedor seleccionado de los combobox
                int posicion = this.cbxProveedor.getSelectedIndex();
                objProveedor = PantallaPrincipal.gProv.getObjProveedor(posicion);

                precioVenta = Float.parseFloat(this.txtPrecioVenta.getText());
                //Obtener la fecha de los combobox
                String dia, mes, anio;
                dia = Integer.toString(this.cbxDia.getSelectedIndex());
                mes = Integer.toString(this.cbxMes.getSelectedIndex());
                anio = this.txtAnio.getText();

                fechaVencimiento = AdminFechas.stringToDate(dia + "-" + mes + "-" + anio);

                //crear y Agregar el objeto a la gestion
                Producto objProducto = new Producto();
                objProducto.setDescripcionProducto(descripcion);
                objProducto.setExistencia(existencia);
                objProducto.setFechaVencimiento(fechaVencimiento);
                //objProducto.setIdProducto(id);
                objProducto.setNombreProducto(nombre);
                objProducto.setPrecioVenta(precioVenta);
                objProducto.setProvedor(objProveedor);
                objProducto.setuMedida(uMedida);

                PantallaPrincipal.gProd.agregarProducto(objProducto);

                actualizarElementosEnTabla();
                this.agregando = false;
                this.btnGuardar.setEnabled(false);
                this.btnEditar.setEnabled(false);
                this.btnEliminar.setEnabled(false);
                this.btnNuevo.setEnabled(true);
                this.btnCancelar.setEnabled(false);

            } else {
                nombre = this.txtNombreProducto.getText();
                descripcion = this.txtDescripcion.getText();
                uMedida = this.txtUnidadMedida.getText();
                existencia = Integer.parseInt(this.txtExistencia.getText());

                //Obtener objeto proveedor seleccionado de los combobox
                int posicion = this.cbxProveedor.getSelectedIndex();
                objProveedor = PantallaPrincipal.gProv.getObjProveedor(posicion);

                precioVenta = Float.parseFloat(this.txtPrecioVenta.getText());
                //Obtener la fecha de los combobox
                String dia, mes, anio;
                dia = Integer.toString(this.cbxDia.getSelectedIndex());
                mes = Integer.toString(this.cbxMes.getSelectedIndex());
                anio = this.txtAnio.getText();

                fechaVencimiento = AdminFechas.stringToDate(dia + "-" + mes + "-" + anio);

                //crear y Agregar el objeto a la gestion
                Producto objModificado = new Producto();
                objModificado.setDescripcionProducto(descripcion);
                objModificado.setExistencia(existencia);
                objModificado.setFechaVencimiento(fechaVencimiento);
                //objModificado.setIdProducto(id);
                objModificado.setNombreProducto(nombre);
                objModificado.setPrecioVenta(precioVenta);
                objModificado.setProvedor(objProveedor);
                objModificado.setuMedida(uMedida);

                PantallaPrincipal.gProd.modificarHabitacion(objModificado, posicionElemento);

                actualizarElementosEnTabla();

                this.btnGuardar.setEnabled(false);
                this.btnEditar.setEnabled(false);
                this.btnEliminar.setEnabled(false);
                this.btnNuevo.setEnabled(true);
            }
            this.btnCancelar.setEnabled(false);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error al registrar "+e);
        }


    }//GEN-LAST:event_btnGuardarActionPerformed


    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:

        this.posicionElemento = PantallaPrincipal.gProv.buscarPorNumIdentidad(this.txtBuscar.getText());
        if (posicionElemento == -1) {
            JOptionPane.showMessageDialog(null, "No se encontro ningun resultado con los parametros de busqueda");
        } else {
            Proveedor provee = PantallaPrincipal.gProv.getObjProveedor(this.posicionElemento);
            this.txtCorreo.setText(provee.getCorreo());
            this.txtID.setText(Integer.toString(provee.getIdProveedor()));
            this.txtNumIdentidad.setText(provee.getNumIden());
            this.txtNumTelefono.setText(provee.getNumTelefono());
            this.txtPrimerApellido.setText(provee.getpApellido());
            this.txtPrimerNombre.setText(provee.getpNombre());

            this.btnEditar.setEnabled(true);
            this.btnEliminar.setEnabled(true);
            this.btnGuardar.setEnabled(false);
        }

    }//GEN-LAST:event_btnBuscarActionPerformed


    /**
     * Metodo que retorna el estado civil
     *
     * @return Estado civil.
     */
    public String estadoCivil() {
        String estCivil = null;
        if (this.jradCasado.isSelected()) {
            estCivil = "Casado";
        } else {
            if (this.jradSoltero.isSelected()) {
                estCivil = "Soltero";
            } else {
                if (this.jradViudo.isSelected()) {
                    estCivil = "Viudo";
                }
            }
        }
        return estCivil;
    }
    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed

        // TODO add your handling code here:
        try {
            
             if (agregando) {
            id = PantallaPrincipal.gProv.AsignarId();
            String nombre, apellido, correo, telefono, estadoCivil, numIdentidad;
            Date fechaNacimmiento;

            String dia, mes, anio;

            nombre = this.txtPrimerNombre.getText();
            apellido = this.txtPrimerApellido.getText();
            correo = this.txtCorreo.getText();
            telefono = this.txtNumTelefono.getText();

            dia = Integer.toString(cbxFecaNacimiento.getSelectedIndex() + 1);
            mes = Integer.toString(cbxMesNacimiento.getSelectedIndex());
            anio = txtAnioNacimiento.getText();

            fechaNacimmiento = AdminFechas.stringToDate(dia + "-" + mes + "-" + anio);

            estadoCivil = this.estadoCivil();
            numIdentidad = this.txtNumIdentidad.getText();

            //crear el objeto
            Proveedor objProv = new Proveedor();
            objProv.setpNombre(nombre);
            objProv.setpApellido(apellido);
            objProv.setNumTelefono(telefono);
            objProv.setNumIden(numIdentidad);
            objProv.setIdProveedor(id);
            objProv.setFechaNacimiento(fechaNacimmiento);
            objProv.setEstadoCivil(estadoCivil);
            objProv.setCorreo(correo);

            //Agregar el objeto a la gestion
            PantallaPrincipal.gProv.agregarProveedor(objProv);

            actualizarElementosEnTabla();
            this.btnGuardar.setEnabled(false);
            this.btnEditar.setEnabled(false);
            this.btnEliminar.setEnabled(false);
            this.btnNuevo.setEnabled(true);
            this.btnCancelar.setEnabled(false);
            agregando = false;
        } else {
            id = PantallaPrincipal.gProv.AsignarId();
            String nombre, apellido, correo, telefono, estadoCivil, numIdentidad;
            Date fechaNacimmiento;

            String dia, mes, anio;

            nombre = this.txtPrimerNombre.getText();
            apellido = this.txtPrimerApellido.getText();
            correo = this.txtCorreo.getText();
            telefono = this.txtNumTelefono.getText();

            dia = Integer.toString(cbxFecaNacimiento.getSelectedIndex() + 1);
            mes = Integer.toString(cbxMesNacimiento.getSelectedIndex());
            anio = txtAnioNacimiento.getText();

            fechaNacimmiento = AdminFechas.stringToDate(dia + "-" + mes + "-" + anio);

            estadoCivil = this.estadoCivil();
            numIdentidad = this.txtNumIdentidad.getText();

            //crear el objeto
            Proveedor objModificado = new Proveedor();
            objModificado.setpNombre(nombre);
            objModificado.setpApellido(apellido);
            objModificado.setNumTelefono(telefono);
            objModificado.setNumIden(numIdentidad);
            objModificado.setIdProveedor(id);
            objModificado.setFechaNacimiento(fechaNacimmiento);
            objModificado.setEstadoCivil(estadoCivil);
            objModificado.setCorreo(correo);

            //Agregar el objeto a la gestion
            PantallaPrincipal.gProv.modificarProveedor(posicionElemento, objModificado);

            actualizarElementosEnTabla();

            this.btnGuardar.setEnabled(false);
            this.btnEditar.setEnabled(false);
            this.btnEliminar.setEnabled(false);
            this.btnNuevo.setEnabled(true);

        }
        this.btnCancelar.setEnabled(false);
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error al registrar "+e);
        }
        
       


    }//GEN-LAST:event_btnGuardarActionPerformed


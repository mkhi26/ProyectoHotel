    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
        this.posicionElemento = PantallaPrincipal.gEmpleado.buscarPorIdentificacion(this.txtBuscar.getText());
        if (posicionElemento == -1) {
            JOptionPane.showConfirmDialog(null, "No se encontro resultado con los parametros de busqueda");
        } else {
            Empleado objEmpleado = PantallaPrincipal.gEmpleado.getObjetoEmpleado(this.posicionElemento);
            this.txtCorreo.setText(objEmpleado.getCorreo());
            this.cbxFechaInicio.setEditable(false);

            //this.cbxFechaInicio.setSelectedIndex(WIDTH);
            // this.txtFechaNacimiento.setText(objEmpleado.getFechaNacimiento().toString());
            this.txtID2.setText(Integer.toString(objEmpleado.getIdEmpleado()));
            this.txtNumIdentidad.setText(objEmpleado.getNumIden());
            this.txtNumTelefono.setText(objEmpleado.getNumTelefono());
            this.txtPrimerApellido.setText(objEmpleado.getpApellido());
            this.txtPrimerNombre.setText(objEmpleado.getpNombre());
            this.txtSueldo.setText(Double.toString(objEmpleado.getSueldo()));

            this.btnEditar.setEnabled(true);
            this.btnEliminar.setEnabled(true);
            this.btnGuardar.setEnabled(false);
        }


    }//GEN-LAST:event_btnBuscarActionPerformed


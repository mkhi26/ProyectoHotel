    private void btnReportesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnReportesActionPerformed
        // TODO add your handling code here:

        String nombreArchivo = "Empleados";
        String contenido = PantallaPrincipal.gEmpleado.getInformacionGestion();
        String ruta = "Reportes/" + nombreArchivo;

        try {
            FileOutputStream archivo = new FileOutputStream(ruta + ".pdf");
            Document doc = new Document();
            PdfWriter.getInstance(doc, archivo);
            doc.open();
            doc.add(new Paragraph(contenido));
            doc.close();
            JOptionPane.showMessageDialog(null, "Reporte generado correctamente");
        } catch (Exception e) {
            System.out.println("error" + e);
        }

    }//GEN-LAST:event_btnReportesActionPerformed

    //Metodo para actualizar los elementos de la tabla
    public void actualizarElementosEnTabla() {

        String[] tituloColumnas = {"ID", "Nombre", "Apellido", "Correo electronico", "Num. telefono", "Num. identidad", "Area de trabajo", "Sueldo"};
        DefaultTableModel tablaEmpleado = new DefaultTableModel(PantallaPrincipal.gEmpleado.getArrayGestion(), tituloColumnas);

        this.jtblEmpleado.setModel(tablaEmpleado);
        AdminSerializacion.guardar(PantallaPrincipal.gEmpleado, "objEmpleado.obj");

    }


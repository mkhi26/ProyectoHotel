                //Asignar el area al empleado
                area = areaTrabajo.getDescripcionarea();

                sueldo = Double.parseDouble(this.txtSueldo.getText());

                //crear el objeto
                Empleado objEmpleado = new Empleado();
                objEmpleado.setpNombre(nombre);
                objEmpleado.setpApellido(apellido);
                objEmpleado.setSueldo(sueldo);
                objEmpleado.setNumTelefono(telefono);

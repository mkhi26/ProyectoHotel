    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:

        this.posicionElemento = PantallaPrincipal.gRsv.buscarPorNumIdentidadCliente(txtBuscar.getText());
        if (posicionElemento == -1) {
            JOptionPane.showMessageDialog(null, "No se encontro reservas con los parametros de busqueda");
        } else {
            Reserva objReserva = PantallaPrincipal.gRsv.getObjetoReserva(posicionElemento);

            this.txtCorreo.setText(objReserva.getCorreo());
            this.txtId.setText(Integer.toString(objReserva.getIdReserva()));
            this.txtNumIdentidad.setText(objReserva.getNumIdentidad());
            this.txtNumTelefono.setText(objReserva.getTelefono());
            this.txtPrimerApellido.setText(objReserva.getApellido());
            this.txtPrimerNombre.setText(objReserva.getNombre());
            this.txtPrecioEstadia.setText(Double.toString(objReserva.getPrecioEstadia()));

            //Encontrar posicion del objeto empleado en el combobox
            int idempleado = objReserva.getDatosEmpleadoEncargado().getIdEmpleado();
            int posicion = PantallaPrincipal.gEmpleado.buscarPorId(idempleado);
            this.cbxEmpleado.setSelectedIndex(posicion);
            //this.cbxFechaEgreso.setSelectedIndex();
            //this.cbxFechaIngreso.setSelectedIndex(WIDTH);

            //Encontrar la posicion del objeto habitacion en la lista
            int idHabitacion = objReserva.getDatosHabitacion().getIdHabitacion();
            int posicionObjHabitacion = PantallaPrincipal.gHab.buscarPorId(idHabitacion);
            this.cbxHabitacion.setSelectedIndex(posicionObjHabitacion);

            //this.cbxMesEgreso.setSelectedIndex(WIDTH);
            //this.cbxMesIngreso.setSelectedIndex(WIDTH);
            this.btnEditar.setEnabled(true);
            this.btnEliminar.setEnabled(true);
            this.btnGuardar.setEnabled(false);
        }


    }//GEN-LAST:event_btnBuscarActionPerformed


                idReserva = PantallaPrincipal.gRsv.AsignarId();
                nombre = this.txtPrimerNombre.getText();
                apellido = this.txtPrimerApellido.getText();
                correo = this.txtCorreo.getText();
                telefono = this.txtNumTelefono.getText();
                numIdentidad = this.txtNumIdentidad.getText();
                habitacion = datosHabitacion.getCategoria().getNombreCategoria();
                empleado = datosEmpleadoEncargado.getpNombre() + " " + datosEmpleadoEncargado.getpApellido();
                numHuesped = (int) this.spnNumHuesped.getValue();
                fechaReserva = AdminFechas.getFechaActual();
                int dia;
                int mes;
                String anio = AdminFechas.getAnioActualString();

                //Para la fecha de ingreso
                dia = cbxFechaIngreso.getSelectedIndex() + 1;
                mes = cbxMesIngreso.getSelectedIndex() + 1;
                fechaIngreso = AdminFechas.stringToDate(Integer.toString(dia) + "-" + Integer.toString(mes) + "-" + anio);

                //Para la fecha de egreso
                dia = cbxFechaEgreso.getSelectedIndex() + 1;
                mes = cbxMesEgreso.getSelectedIndex() + 1;
                fechaEgreso = AdminFechas.stringToDate(Integer.toString(dia) + "-" + Integer.toString(mes) + "-" + anio);

                precio = Double.parseDouble(this.txtPrecioEstadia.getText());
                //Crear el objeto
                Reserva objModificado = new Reserva();
                objModificado.setApellido(apellido);
                objModificado.setCorreo(correo);
                objModificado.setDatosEmpleadoEncargado(datosEmpleadoEncargado);
                objModificado.setDatosHabitacion(datosHabitacion);
                objModificado.setFechaEgreso(fechaEgreso);

    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        // TODO add your handling code here:

        try {
            String correo, contrasenia;
            correo = txtCorreoElecronico.getText();
            contrasenia = txtContrasenia.getText();

            int posicion = PantallaPrincipal.gUsuario.buscarPorCorreo(correo);
            if (posicion == -1) {
                JOptionPane.showMessageDialog(null, "Correo o contraseña incorrectos");
                this.lblMensaje.setVisible(true);
            } else {
                Usuario objUsuario = new Usuario();

                objUsuario = PantallaPrincipal.gUsuario.getUsuario(posicion);
                if (objUsuario.getContrasenia().equals(contrasenia)) {
                    JOptionPane.showMessageDialog(null, "Inicio de secion correctamente");
                    PantallaPrincipal.MenuHotel.setEnabled(true);
                    PantallaPrincipal.MenuEmpleado.setEnabled(true);
                    PantallaPrincipal.MenuHospedaje.setEnabled(true);
                    PantallaPrincipal.MenuProductos.setEnabled(true);
                    PantallaPrincipal.mnuPago.setEnabled(true);
                    PantallaPrincipal.menuCerrarSesion.setEnabled(true);
                    dispose();

                } else {
                    JOptionPane.showMessageDialog(null, "Error al iniciar secion");
                    login = true;
                }

            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error al iniciar secion "+e);
        }


    }//GEN-LAST:event_btnRegistrarActionPerformed


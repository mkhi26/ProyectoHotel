    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
        String accion = this.cbxBuscar.getSelectedItem().toString();
        if (accion.equalsIgnoreCase("Buscar por ID")) {
            id = Integer.parseInt(txtBuscar.getText());
            this.posicionElemento = PantallaPrincipal.gCatHab.buscarPorId(id);
            CategoriaHabitacion objCatHab = PantallaPrincipal.gCatHab.objCategoriaHabitacion(posicionElemento);

            this.txtID.setText(Integer.toString(objCatHab.getIdCategoria()));
            this.txtNombreCategoria.setText(objCatHab.getNombreCategoria());
            this.txtDescripcion.setText(objCatHab.getCaracteristicas());
            this.txtPrecioDia.setText(Double.toString(objCatHab.getPrecioDia()));

            this.txtDescripcion.setEditable(false);
            this.txtNombreCategoria.setEditable(false);
            this.txtPrecioDia.setEditable(false);
            this.txtID.setEditable(false);

            this.txtDescripcion.setEnabled(false);
            this.txtNombreCategoria.setEnabled(false);
            this.txtPrecioDia.setEnabled(false);

            this.btnEditar.setEnabled(true);
            this.btnEliminar.setEnabled(true);
            this.btnGuardar.setEnabled(false);
            this.btnCancelar.setEnabled(true);

        } else {
            String nombre = txtBuscar.getText();
            this.posicionElemento = PantallaPrincipal.gCatHab.buscarPornombre(nombre);
            CategoriaHabitacion objCatHab = PantallaPrincipal.gCatHab.objCategoriaHabitacion(posicionElemento);

            this.txtID.setText(Integer.toString(objCatHab.getIdCategoria()));
            this.txtNombreCategoria.setText(objCatHab.getNombreCategoria());
            this.txtDescripcion.setText(objCatHab.getCaracteristicas());
            this.txtPrecioDia.setText(Double.toString(objCatHab.getPrecioDia()));

            this.txtDescripcion.setEditable(false);
            this.txtNombreCategoria.setEditable(false);
            this.txtPrecioDia.setEditable(false);
            this.txtID.setEditable(false);

            this.txtDescripcion.setEnabled(false);
            this.txtNombreCategoria.setEnabled(false);
            this.txtPrecioDia.setEnabled(false);

            this.btnEditar.setEnabled(true);
            this.btnEliminar.setEnabled(true);
            this.btnGuardar.setEnabled(false);
            this.btnCancelar.setEnabled(true);

        }


    }//GEN-LAST:event_btnBuscarActionPerformed


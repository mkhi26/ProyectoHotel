    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed

        // TODO add your handling code here:
        try {
            if (agregando) {
                /*
            1. Obtener el valor de los textbox
            2. Crear el objeto
            3. Agregarlo a la gestion
            4. Mostrar el elementos registrado
                 */
                //1. Obtener el valor de los textbox

                int id, capacidad;
                double precio;
                String nombre, caracteristicas;
                id = PantallaPrincipal.gCatHab.AsignarId();
                nombre = this.txtNombreCategoria.getText();
                caracteristicas = this.txtDescripcion.getText();
                precio = Double.parseDouble(this.txtPrecioDia.getText());
                capacidad = Integer.parseInt(cbxCapacidad.getSelectedItem().toString());

                //2.Crear el objeto CategoriaHabitacion
                CategoriaHabitacion catHab = new CategoriaHabitacion();
                catHab.setIdCategoria(id);
                catHab.setCapacidad(capacidad);
                catHab.setNombreCategoria(nombre);
                catHab.setCaracteristicas(caracteristicas);
                catHab.setCapacidad(capacidad);
                catHab.setPrecioDia(precio);

                //3. Agregarlo a la gestion
                PantallaPrincipal.gCatHab.agregarCatHabitacion(catHab);
                //4 Mostrar el elemento registrado.
                this.actualizarElementosEnTabla();
                this.agregando = false;
                this.btnGuardar.setEnabled(false);
                this.btnEditar.setEnabled(false);
                this.btnEliminar.setEnabled(false);
                this.btnNuevo.setEnabled(true);
                this.btnCancelar.setEnabled(false);
            } else {
                int id, capacidad;
                double precio;
                String nombre, caracteristicas;
                id = PantallaPrincipal.gCatHab.AsignarId();
                nombre = this.txtNombreCategoria.getText();
                caracteristicas = this.txtDescripcion.getText();
                precio = Double.parseDouble(this.txtPrecioDia.getText());
                capacidad = Integer.parseInt(cbxCapacidad.getSelectedItem().toString());

                //2.Crear el objeto CategoriaHabitacion
                CategoriaHabitacion objModificado = new CategoriaHabitacion();
                objModificado.setCapacidad(capacidad);
                objModificado.setIdCategoria(id);
                objModificado.setNombreCategoria(nombre);
                objModificado.setCaracteristicas(caracteristicas);
                objModificado.setCapacidad(capacidad);
                objModificado.setPrecioDia(precio);

                //3. Agregarlo a la gestion
                PantallaPrincipal.gCatHab.modfCatHabitacion(posicionElemento, objModificado);
                //4 Mostrar el elemento registrado.
                this.actualizarElementosEnTabla();
                this.btnGuardar.setEnabled(false);
                this.btnEditar.setEnabled(false);
                this.btnEliminar.setEnabled(false);
                this.btnNuevo.setEnabled(true);
            }
            this.txtDescripcion.setEnabled(false);
            this.txtID.setEditable(false);
            this.txtNombreCategoria.setEnabled(false);
            this.txtPrecioDia.setEnabled(false);
            this.btnCancelar.setEnabled(false);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error al registrar"+e);
        }


    }//GEN-LAST:event_btnGuardarActionPerformed

